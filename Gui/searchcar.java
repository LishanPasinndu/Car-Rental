    public void loadcars() {

        try {

            ResultSet rs = MySQL.search("SELECT * FROM `car` INNER JOIN `brand` ON `brand`.`id`=`car`.`brand_id` INNER JOIN `fuel_type` ON `fuel_type`.`id`=`car`.`fuel_type_id` WHERE `car`.`status_id`='1' ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("model"));
                v.add(rs.getString("number"));
                v.add(rs.getString("fuel_type.name"));
                v.add(rs.getString("price"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

