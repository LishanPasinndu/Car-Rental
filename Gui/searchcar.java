    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:

        int r = jTable1.getSelectedRow();

        if (r == -1) {
            JOptionPane.showMessageDialog(this, "Please Select a Car", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            String brand = jTable1.getValueAt(r, 1).toString();
            String model = jTable1.getValueAt(r, 2).toString();
            String number = jTable1.getValueAt(r, 3).toString();
            String fuel = jTable1.getValueAt(r, 4).toString();
            String price = jTable1.getValueAt(r, 5).toString();

            try {
                ResultSet rs1 = MySQL.search("SELECT * FROM `brand` WHERE `name`='" + brand + "' ");
                rs1.next();

                int brandid = rs1.getInt("id");

                ResultSet rs2 = MySQL.search("SELECT * FROM `fuel_type` WHERE `name`='" + fuel + "' ");
                rs2.next();

                int fuelid = rs2.getInt("id");

                if (uc != null) {

                    uc.jComboBox1.setSelectedIndex(brandid);
                    uc.jComboBox2.setSelectedIndex(fuelid);
                    uc.jTextField1.setText(number);
                    uc.jTextField2.setText(model);
                    uc.jTextField3.setText(price);

                    this.dispose();

                }

            } catch (Exception e) {
            }

        }

    }//GEN-LAST:event_jTable1MouseClicked

