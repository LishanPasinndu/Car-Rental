    public void searchCars() {

        try {

            int brand = jComboBox1.getSelectedIndex();
            String model = jTextField2.getText();
            String number = jTextField1.getText();
            int fuel = jComboBox2.getSelectedIndex();
            String price = jTextField3.getText();

            Vector queryvector = new Vector();

            if (brand == 0) {
                loadcars();
            } else {
                queryvector.add("`car`.`brand_id`='" + brand + "' ");
            }

            if (fuel == 0) {
                loadcars();
            } else {
                queryvector.add("`car`.`fuel_type_id`='" + fuel + "' ");
            }

            if (model.isEmpty()) {
                loadcars();
            } else {
                queryvector.add("`car`.`model` LIKE '%" + model + "%' ");
            }

            if (number.isEmpty()) {
                loadcars();
            } else {
                queryvector.add("`car`.`number` LIKE '%" + number + "%' ");
            }

            if (price.isEmpty()) {
                loadcars();
            } else {
                queryvector.add("`car`.`price` LIKE '%" + price + "%' ");
            }

            String where = "WHERE";

            for (int i = 0; i < queryvector.size(); i++) {
                where += " ";
                where += queryvector.get(i);
                where += " ";
                if (i != queryvector.size() - 1) {
                    where += "AND";
                }
            }

            ResultSet rs = MySQL.search("SELECT * FROM `car` INNER JOIN `brand` ON `brand`.`id`=`car`.`brand_id` INNER JOIN `fuel_type` ON `fuel_type`.`id`=`car`.`fuel_type_id` " + where + " ; ");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("brand.name"));
                v.add(rs.getString("model"));
                v.add(rs.getString("number"));
                v.add(rs.getString("fuel_type.name"));
                v.add(rs.getString("price"));

                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

